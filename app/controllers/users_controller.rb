class UsersController < ApplicationController
  # GET /users
  # GET /users.json
  def index
    @users = Tapir::Entities::User.all

    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @users }
    end
  end

  # GET /users/1
  # GET /users/1.json
  def show
    @user = Tapir::Entities::User.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @user }
    end
  end

  # GET /users/new
  # GET /users/new.json
  def new
    @user = Tapir::Entities::User.new
    @usernames_array_or_nil = []
    @usernames_array_or_nil = @user.usernames.join(",") if @user.usernames

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @user }
    end
  end

  # GET /users/1/edit
  def edit
    @user = Tapir::Entities::User.find(params[:id])
  end

  # POST /users
  # POST /users.json
  def create
    @user = Tapir::Entities::User.new(params[:tapir_entities_user])
    
    respond_to do |format|
      if @user.save
        format.html { redirect_to @user, notice: 'User was successfully created.' }
        format.json { render json: @user, status: :created, location: @user }
      else
        format.html { render action: "new" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /users/1
  # PUT /users/1.json
  def update
    @user = Tapir::Entities::User.find(params[:id])

    respond_to do |format|
      if @user.update_attributes(params[:tapir_entities_user])
        format.html { redirect_to @user, notice: 'User was successfully updated.' }
        format.json { head :ok }
      else
        format.html { render action: "edit" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    @user = Tapir::Entities::User.find(params[:id])
    @user.destroy

    respond_to do |format|
      format.html { redirect_to users_url }
      format.json { head :ok }
    end
  end
end
